    fn space_glyph_is_all_zeros() {
        let g = glyph(b' ');
        assert!(g.iter().all(|&b| b == 0));
    }

    #[test]
    fn non_printable_falls_back_to_question_mark() {
        let fallback = glyph(0x01);
        let question = glyph(b'?');
        assert_eq!(fallback, question);
    }

