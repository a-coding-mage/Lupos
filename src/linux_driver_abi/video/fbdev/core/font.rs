/// Embedded 8×16 VGA bitmap font for framebuffer text rendering.
///
/// This is the standard VGA BIOS font (Code Page 437) that has been used
/// on IBM PC-compatible machines since 1981.  Linux embeds the same font
/// in `lib/fonts/font_8x16.c` for its framebuffer console (fbcon).
///
/// Each glyph is 8 pixels wide × 16 pixels tall, stored as 16 bytes
/// (one byte per row, MSB = leftmost pixel).  Only printable ASCII
/// (0x20 ' ' through 0x7E '~') is included — 95 glyphs × 16 bytes = 1520 bytes.
///
/// Ref: Linux lib/fonts/font_8x16.c — fontdata_8x16[]
///      https://wiki.osdev.org/PC_Screen_Font
///      https://int10h.org/oldschool-pc-fonts/fontlist/

/// Width of each glyph in pixels.
pub const GLYPH_WIDTH: usize = 8;

/// Height of each glyph in pixels (scanlines).
pub const GLYPH_HEIGHT: usize = 16;

/// First ASCII code in the font table.
const FIRST_CHAR: u8 = 0x20; // space

/// Last ASCII code in the font table.
const LAST_CHAR: u8 = 0x7E; // tilde

/// Number of glyphs in the font.
const GLYPH_COUNT: usize = (LAST_CHAR - FIRST_CHAR + 1) as usize; // 95

/// Get the glyph bitmap for an ASCII character.
///
/// Returns a slice of 16 bytes (one per row).  For characters outside
/// the printable ASCII range, returns the glyph for '?' (0x3F).
pub fn glyph(ch: u8) -> &'static [u8; GLYPH_HEIGHT] {
    let idx = if ch >= FIRST_CHAR && ch <= LAST_CHAR {
        (ch - FIRST_CHAR) as usize
    } else {
        ('?' as u8 - FIRST_CHAR) as usize // fallback to '?'
    };
    // Safety: idx is always in range [0, GLYPH_COUNT)
    let offset = idx * GLYPH_HEIGHT;
    // Convert a slice reference to an array reference
    FONT_DATA[offset..offset + GLYPH_HEIGHT].try_into().unwrap()
}

