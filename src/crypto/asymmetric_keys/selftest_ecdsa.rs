#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct EcdsaSelftestVectorMeta {
    pub name: &'static str,
    pub keys_len: usize,
    pub data_len: usize,
    pub sig_len: usize,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct EcdsaSelftestVector {
    pub name: &'static str,
    pub keys: &'static [u8],
    pub data: &'static [u8],
    pub sig: &'static [u8],
}

pub const ECDSA_SELFTEST_VECTOR: EcdsaSelftestVectorMeta = EcdsaSelftestVectorMeta {
    name: SELFTEST_NAME,
    keys_len: CERTS_SELFTEST_ECDSA_KEYS_DER_LEN,
    data_len: CERTS_SELFTEST_ECDSA_DATA_LEN,
    sig_len: CERTS_SELFTEST_ECDSA_SIG_DER_LEN,
};

pub const ECDSA_SELFTEST_BYTES: EcdsaSelftestVector = EcdsaSelftestVector {
    name: SELFTEST_NAME,
    keys: &CERTS_SELFTEST_ECDSA_KEYS,
    data: &CERTS_SELFTEST_ECDSA_DATA,
    sig: &CERTS_SELFTEST_ECDSA_SIG,
};

pub fn fips_signature_selftest_ecdsa()
-> Result<FipsSignatureSelftestReport, FipsSignatureSelftestPanic> {
    fips_signature_selftest(
        SELFTEST_NAME,
        &CERTS_SELFTEST_ECDSA_KEYS,
        &CERTS_SELFTEST_ECDSA_DATA,
        &CERTS_SELFTEST_ECDSA_SIG,
    )
}

pub fn fips_signature_selftest_ecdsa_with_results(
    results: FipsSignatureSelftestResults,
) -> Result<FipsSignatureSelftestReport, FipsSignatureSelftestPanic> {
    fips_signature_selftest_with_results(
        SELFTEST_NAME,
        &CERTS_SELFTEST_ECDSA_KEYS,
        &CERTS_SELFTEST_ECDSA_DATA,
        &CERTS_SELFTEST_ECDSA_SIG,
        results,
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    static TEST_LOCK: spin::Mutex<()> = spin::Mutex::new(());

    #[test]
    fn selftest_ecdsa_matches_linux_vector_shape() {
        let _lsm_guard = crate::security::lsm_list::TEST_LSM_LOCK.lock();
        let source = include_str!(concat!(
            env!("CARGO_MANIFEST_DIR"),
            "/vendor/linux/crypto/asymmetric_keys/selftest_ecdsa.c"
        ));
        let header = include_str!(concat!(
            env!("CARGO_MANIFEST_DIR"),
            "/vendor/linux/crypto/asymmetric_keys/selftest.h"
        ));
        assert!(source.contains("#include \"selftest.h\""));
        assert!(source.contains("static const u8 certs_selftest_ecdsa_keys[] __initconst"));
        assert!(source.contains("/* P-256 ECDSA certificate */"));
        assert!(source.contains("static const u8 certs_selftest_ecdsa_data[] __initconst"));
        assert!(source.contains("static const u8 certs_selftest_ecdsa_sig[] __initconst"));
        assert!(source.contains("/* ECDSA signature using SHA-256 */"));
        assert!(source.contains("void __init fips_signature_selftest_ecdsa(void)"));
        assert!(source.contains("fips_signature_selftest(\"ECDSA\""));
        assert!(source.contains("sizeof(certs_selftest_ecdsa_keys) - 1"));
        assert!(source.contains("sizeof(certs_selftest_ecdsa_data) - 1"));
        assert!(source.contains("sizeof(certs_selftest_ecdsa_sig) - 1"));
        assert!(header.contains("void __init fips_signature_selftest_ecdsa(void);"));

        assert_eq!(ECDSA_SELFTEST_VECTOR.name, "ECDSA");
        assert_eq!(ECDSA_SELFTEST_VECTOR.keys_len, 472);
        assert_eq!(ECDSA_SELFTEST_VECTOR.data_len, 71);
        assert_eq!(ECDSA_SELFTEST_VECTOR.sig_len, 247);
    }

    #[test]
    fn selftest_ecdsa_vectors_are_the_linux_payloads_without_c_nul() {
        let _lsm_guard = crate::security::lsm_list::TEST_LSM_LOCK.lock();
        assert_eq!(CERTS_SELFTEST_ECDSA_KEYS.len(), 472);
        assert_eq!(CERTS_SELFTEST_ECDSA_KEYS[..4], [0x30, 0x82, 0x01, 0xd4]);
        assert_eq!(CERTS_SELFTEST_ECDSA_KEYS[468..], [0x1c, 0x7a, 0xd7, 0x5d]);

        assert_eq!(CERTS_SELFTEST_ECDSA_DATA.len(), 71);
        assert_eq!(&CERTS_SELFTEST_ECDSA_DATA[..20], b"This is some test da");
        assert_eq!(CERTS_SELFTEST_ECDSA_DATA[70], b'\n');

        assert_eq!(CERTS_SELFTEST_ECDSA_SIG.len(), 247);
        assert_eq!(CERTS_SELFTEST_ECDSA_SIG[..4], [0x30, 0x81, 0xf4, 0x06]);
        assert_eq!(CERTS_SELFTEST_ECDSA_SIG[244..], [0xfd, 0xa2, 0xf1]);
    }

    #[test]
    fn ecdsa_wrapper_invokes_shared_fips_signature_selftest() {
        let _lsm_guard = crate::security::lsm_list::TEST_LSM_LOCK.lock();
        let _guard = TEST_LOCK.lock();
        crate::security::keys::reset_for_test();
        crate::security::certs::reset_for_test();

        let report = fips_signature_selftest_ecdsa().expect("ecdsa selftest");
        assert_eq!(report.name, SELFTEST_NAME);
        assert_eq!(report.detached_data_len, CERTS_SELFTEST_ECDSA_DATA_LEN);
        assert_eq!(report.signature_len, CERTS_SELFTEST_ECDSA_SIG_DER_LEN);
        assert!(report.loaded_certificates >= 1);
        assert!(report.pkcs7_parsed);
        assert!(report.detached_data_supplied);
        assert!(report.verified);
        assert!(report.trusted);
        assert!(report.pkcs7_freed);
        assert!(report.keyring_put);
    }

    #[test]
    fn ecdsa_wrapper_preserves_shared_error_paths() {
        let _lsm_guard = crate::security::lsm_list::TEST_LSM_LOCK.lock();
        let _guard = TEST_LOCK.lock();
        crate::security::keys::reset_for_test();
        crate::security::certs::reset_for_test();

        let err = fips_signature_selftest_ecdsa_with_results(FipsSignatureSelftestResults {
            pkcs7_verify_ret: -22,
            ..FipsSignatureSelftestResults::SUCCESS
        });
        assert!(matches!(
            err,
            Err(FipsSignatureSelftestPanic::Pkcs7Verify { name, errno })
                if name == SELFTEST_NAME && errno == -22
        ));
    }
}
