    #[test]
    fn teardown_errno_boundary_accepts_either_internal_sign() {
        assert_eq!(normalize_errno(EBUSY), EBUSY);
        assert_eq!(normalize_errno(-EBUSY), EBUSY);
        assert_eq!(normalize_errno(i32::MIN), EINVAL);
    }

    #[test]
    fn teardown_progress_retries_only_failed_and_later_phases() {
        let progress = ModuleTeardownProgress::default();
        let first_hits = AtomicU32::new(0);
        let retry_hits = AtomicU32::new(0);

        progress
            .run(TEARDOWN_RAW_TRACEPOINTS, || {
                first_hits.fetch_add(1, Ordering::Relaxed);
                Ok(())
            })
            .unwrap();
        assert_eq!(
            progress.run(TEARDOWN_KPROBES, || {
                retry_hits.fetch_add(1, Ordering::Relaxed);
                Err(EBUSY)
            }),
            Err(EBUSY)
        );

        progress
            .run(TEARDOWN_RAW_TRACEPOINTS, || {
                first_hits.fetch_add(1, Ordering::Relaxed);
                Ok(())
            })
            .unwrap();
        progress
            .run(TEARDOWN_KPROBES, || {
                retry_hits.fetch_add(1, Ordering::Relaxed);
                Ok(())
            })
            .unwrap();
        assert_eq!(first_hits.load(Ordering::Relaxed), 1);
        assert_eq!(retry_hits.load(Ordering::Relaxed), 2);
    }

    fn write_i32(data: &mut [u8], offset: usize, value: i32) {
        data[offset..offset + 4].copy_from_slice(&value.to_le_bytes());
    }

    fn write_u16(data: &mut [u8], offset: usize, value: u16) {
        data[offset..offset + 2].copy_from_slice(&value.to_le_bytes());
    }

    fn write_u32(data: &mut [u8], offset: usize, value: u32) {
        data[offset..offset + 4].copy_from_slice(&value.to_le_bytes());
    }

    fn write_u64(data: &mut [u8], offset: usize, value: u64) {
        data[offset..offset + 8].copy_from_slice(&value.to_le_bytes());
    }

    fn minimal_module_elf(section_count: u16, shstrndx: u16) -> Vec<u8> {
        let mut elf = alloc::vec![0u8; 64 + section_count as usize * 64];
        elf[0..4].copy_from_slice(&ELF_MAGIC);
        elf[4] = ELFCLASS64;
        elf[5] = ELFDATA2LSB;
        write_u16(&mut elf, 16, ET_REL);
        write_u16(&mut elf, 18, EM_X86_64);
        write_u64(&mut elf, 40, 64);
        write_u16(&mut elf, 58, 64);
        write_u16(&mut elf, 60, section_count);
        write_u16(&mut elf, 62, shstrndx);
        elf
    }

    fn write_shdr(
        data: &mut [u8],
        index: usize,
        name_idx: u32,
        sh_type: u32,
        offset: u64,
        size: u64,
    ) {
        let base = 64 + index * 64;
        write_u32(data, base, name_idx);
        write_u32(data, base + 4, sh_type);
        write_u64(data, base + 24, offset);
        write_u64(data, base + 32, size);
    }

    fn prel32(from: usize, to: usize) -> i32 {
        let delta = to as isize - from as isize;
        assert!(
            delta >= i32::MIN as isize && delta <= i32::MAX as isize,
            "test sections must be within the x86 module PREL32 window: from={from:#x} to={to:#x} delta={delta}"
        );
        delta as i32
    }

