/// Values of `enum module_state` from `vendor/linux/include/linux/module.h`.
///
/// The value is stored in the relocated C `struct module`, not duplicated in
/// the Rust owner.  `THIS_MODULE`, module users and the loader therefore all
/// observe the same state word, as they do in Linux.
#[repr(u32)]
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum ModuleState {
    Live = 0,
    Coming = 1,
    Going = 2,
    Unformed = 3,
}

/// Loaded ELF section memory.
///
/// Linux allocates module sections through `module_memory_alloc()` and
/// architecture execmem ranges (`vendor/linux/kernel/module/main.c`,
/// `vendor/linux/arch/x86/mm/init.c`). Runtime Lupos follows that contract so
/// x86 module relocations see addresses inside `MODULES_VADDR..MODULES_END`;
/// host unit tests keep ordinary `Vec` storage because they do not install
/// kernel page tables.
pub struct LoadedSection {
    #[cfg(test)]
    data: Vec<u8>,
    #[cfg(test)]
    borrowed: *mut u8,
    #[cfg(not(test))]
    ptr: *mut u8,
    len: usize,
}

unsafe impl Send for LoadedSection {}
unsafe impl Sync for LoadedSection {}

pub(crate) struct NameMap<T> {
    entries: Vec<(String, T)>,
}

impl<T> NameMap<T> {
    pub(crate) fn new() -> Self {
        Self {
            entries: Vec::new(),
        }
    }

    pub(crate) fn insert(&mut self, name: String, value: T) -> Option<T> {
        if let Some((_, existing)) = self.entries.iter_mut().find(|(key, _)| key == &name) {
            return Some(core::mem::replace(existing, value));
        }
        self.entries.push((name, value));
        None
    }

    pub(crate) fn get(&self, name: &str) -> Option<&T> {
        self.entries
            .iter()
            .find(|(key, _)| key == name)
            .map(|(_, value)| value)
    }

    pub(crate) fn get_mut(&mut self, name: &str) -> Option<&mut T> {
        self.entries
            .iter_mut()
            .find(|(key, _)| key == name)
            .map(|(_, value)| value)
    }

    pub(crate) fn contains_key(&self, name: &str) -> bool {
        self.get(name).is_some()
    }

    pub(crate) fn remove(&mut self, name: &str) -> Option<T> {
        self.entries
            .iter()
            .position(|(key, _)| key == name)
            .map(|index| self.entries.remove(index).1)
    }

    pub(crate) fn keys(&self) -> impl Iterator<Item = &String> {
        self.entries.iter().map(|(key, _)| key)
    }

    pub(crate) fn values(&self) -> impl Iterator<Item = &T> {
        self.entries.iter().map(|(_, value)| value)
    }

    pub(crate) fn values_mut(&mut self) -> impl Iterator<Item = &mut T> {
        self.entries.iter_mut().map(|(_, value)| value)
    }

    pub(crate) fn iter(&self) -> impl Iterator<Item = (&String, &T)> {
        self.entries.iter().map(|(key, value)| (key, value))
    }
}

impl<T> core::ops::Index<&str> for NameMap<T> {
    type Output = T;

    fn index(&self, index: &str) -> &Self::Output {
        self.get(index).expect("module section must exist")
    }
}

impl LoadedSection {
    fn new_zeroed(len: usize) -> Result<Self, LoadModuleError> {
        #[cfg(test)]
        {
            Ok(Self {
                data: alloc::vec![0u8; len],
                borrowed: core::ptr::null_mut(),
                len,
            })
        }

        #[cfg(not(test))]
        {
            if len == 0 {
                return Ok(Self {
                    ptr: NonNull::<u8>::dangling().as_ptr(),
                    len,
                });
            }
            let ptr = crate::arch::x86::mm::init::execmem_alloc_rw(len);
            if ptr.is_null() {
                return Err(LoadModuleError::OutOfMemory);
            }
            Ok(Self { ptr, len })
        }
    }

    pub(crate) fn from_bytes(bytes: &[u8]) -> Result<Self, LoadModuleError> {
        let mut section = Self::new_zeroed(bytes.len())?;
        section.as_mut_slice().copy_from_slice(bytes);
        Ok(section)
    }

    /// Build several host-test sections inside one retained allocation.
    ///
    /// A Linux `kernel_symbol` uses signed PREL32 offsets, while independent
    /// glibc allocations can land in arenas more than 2 GiB apart.  Runtime
    /// sections use the contiguous x86 module window; this helper lets the
    /// corresponding host test model that invariant deterministically.
    #[cfg(test)]
    pub(crate) fn borrowed_for_prel32_test(bytes: &mut [u8]) -> Self {
        Self {
            data: Vec::new(),
            borrowed: bytes.as_mut_ptr(),
            len: bytes.len(),
        }
    }

    pub(crate) fn as_ptr(&self) -> *const u8 {
        #[cfg(test)]
        {
            if self.borrowed.is_null() {
                self.data.as_ptr()
            } else {
                self.borrowed
            }
        }

        #[cfg(not(test))]
        {
            self.ptr as *const u8
        }
    }

    fn as_mut_ptr(&mut self) -> *mut u8 {
        #[cfg(test)]
        {
            if self.borrowed.is_null() {
                self.data.as_mut_ptr()
            } else {
                self.borrowed
            }
        }

        #[cfg(not(test))]
        {
            self.ptr
        }
    }

    pub(crate) fn as_slice(&self) -> &[u8] {
        #[cfg(test)]
        {
            if self.borrowed.is_null() {
                self.data.as_slice()
            } else {
                // SAFETY: the test-only constructor receives a live mutable
                // slice, and its caller retains that backing allocation until
                // after every section view is dropped.
                unsafe { core::slice::from_raw_parts(self.borrowed, self.len) }
            }
        }

        #[cfg(not(test))]
        unsafe {
            core::slice::from_raw_parts(self.as_ptr(), self.len)
        }
    }

    pub(crate) fn as_mut_slice(&mut self) -> &mut [u8] {
        #[cfg(test)]
        {
            if self.borrowed.is_null() {
                self.data.as_mut_slice()
            } else {
                // SAFETY: the PREL32 test creates disjoint section views into
                // its retained backing allocation.
                unsafe { core::slice::from_raw_parts_mut(self.borrowed, self.len) }
            }
        }

        #[cfg(not(test))]
        unsafe {
            core::slice::from_raw_parts_mut(self.as_mut_ptr(), self.len)
        }
    }

    fn set_final_permissions(&self, flags: u64) -> Result<(), LoadModuleError> {
        if self.len == 0 {
            return Ok(());
        }
        let writable = flags & SHF_WRITE != 0;
        let executable = flags & SHF_EXECINSTR != 0;
        if writable && executable {
            return Err(LoadModuleError::UnsupportedSection(String::from(
                "writable executable module section",
            )));
        }
        crate::arch::x86::mm::init::execmem_set_final_permissions(
            self.as_ptr().cast_mut(),
            self.len,
            writable,
            executable,
        )
        .map_err(|_| LoadModuleError::UnsupportedSection(String::from("module W^X")))
    }

    pub(crate) const fn len(&self) -> usize {
        self.len
    }
}

#[cfg(not(test))]
impl Drop for LoadedSection {
    fn drop(&mut self) {
        if self.len != 0 {
            crate::arch::x86::mm::init::execmem_free(self.ptr);
        }
    }
}

/// Owns every generic subsystem registration whose backing addresses live in
/// a loaded module.  Linux drives these through the module notifier chain;
/// this owner provides the same COMING/LIVE/GOING lifetime and also makes all
/// partially completed formation paths unwind safely.
struct ModuleRuntimeMetadata {
    owner: usize,
    module_name: String,
    core_text: Vec<AddressRange>,
    init_text: Vec<AddressRange>,
    _btf: Option<ModuleBtf>,
    kallsyms_registered: AtomicBool,
    teardown_progress: ModuleTeardownProgress,
    released: AtomicBool,
}

const TEARDOWN_RAW_TRACEPOINTS: u32 = 1 << 0;
const TEARDOWN_KPROBES: u32 = 1 << 1;
const TEARDOWN_TRACE_EVENTS: u32 = 1 << 2;
const TEARDOWN_TRACEPOINTS: u32 = 1 << 3;
const TEARDOWN_FTRACE: u32 = 1 << 4;

/// Records successful notifier phases so a failed module teardown retries
/// only the failed and not-yet-run work.  Individual subsystems remain
/// idempotent for partial failure inside a phase, but completed phases no
/// longer need to be invoked again on every userspace retry.
#[derive(Default)]
struct ModuleTeardownProgress {
    completed: AtomicU32,
}

impl ModuleTeardownProgress {
    fn run(&self, phase: u32, action: impl FnOnce() -> Result<(), i32>) -> Result<(), i32> {
        if self.completed.load(Ordering::Acquire) & phase != 0 {
            return Ok(());
        }
        action()?;
        self.completed.fetch_or(phase, Ordering::Release);
        Ok(())
    }
}

impl ModuleRuntimeMetadata {
    fn new(
        owner: usize,
        module_name: &str,
        core_text: Vec<AddressRange>,
        init_text: Vec<AddressRange>,
        btf: Option<ModuleBtf>,
    ) -> Self {
        Self {
            owner,
            module_name: String::from(module_name),
            core_text,
            init_text,
            _btf: btf,
            kallsyms_registered: AtomicBool::new(false),
            teardown_progress: ModuleTeardownProgress::default(),
            released: AtomicBool::new(false),
        }
    }

    fn mark_kallsyms_registered(&self) {
        self.kallsyms_registered.store(true, Ordering::Release);
    }

    fn live(&self) -> Result<(), i32> {
        crate::kernel::trace::kprobe::module_live(self.owner, &self.init_text)
    }

    fn begin_going(&self) -> Result<(), i32> {
        crate::kernel::bpf::module_raw_tracepoints::module_begin_going(self.owner)
            .map_err(normalize_errno)
    }

    fn release(&self) -> Result<(), i32> {
        if self.released.load(Ordering::Acquire) {
            return Ok(());
        }

        // Raw tracepoint users hold the same unload-preventing reference as
        // Linux.  Do not dismantle any other registry until that pin check
        // succeeds.
        self.teardown_progress.run(TEARDOWN_RAW_TRACEPOINTS, || {
            crate::kernel::bpf::module_raw_tracepoints::module_going(self.owner)
                .map_err(normalize_errno)
        })?;
        // Restore every probed instruction and wait for displaced execution
        // before any registry or executable module allocation is released.
        self.teardown_progress.run(TEARDOWN_KPROBES, || {
            crate::kernel::trace::kprobe::module_going(self.owner, &self.core_text, &self.init_text)
                .map_err(normalize_errno)
        })?;
        self.teardown_progress.run(TEARDOWN_TRACE_EVENTS, || {
            crate::kernel::trace::trace_events::module_going(self.owner).map_err(normalize_errno)
        })?;
        self.teardown_progress.run(TEARDOWN_TRACEPOINTS, || {
            crate::kernel::trace::tracepoint::module_going(self.owner).map_err(normalize_errno)
        })?;
        // Keep ftrace records and module allocations owned until every
        // instrumented callsite has been restored. A failed text poke leaves
        // the descriptor in GOING so delete_module() can retry this step.
        self.teardown_progress.run(TEARDOWN_FTRACE, || {
            crate::kernel::trace::ftrace::release_module(self.owner).map_err(normalize_errno)
        })?;
        if self.released.swap(true, Ordering::AcqRel) {
            return Ok(());
        }

        crate::kernel::dynamic_debug::module_going(self.owner);
        crate::kernel::printk::index::module_going(self.owner);
        crate::kernel::error_injection::module_going(self.owner);
        if self.kallsyms_registered.swap(false, Ordering::AcqRel) {
            crate::kernel::module::kallsyms::unregister(&self.module_name);
        }
        Ok(())
    }
}

/// The public module-loader API follows the kernel syscall convention and
/// returns a positive errno. Trace/notifier internals follow Linux helpers and
/// commonly return `-errno`; normalize the boundary so sys_delete_module()
/// never turns a teardown failure into a positive syscall result.
fn normalize_errno(error: i32) -> i32 {
    error.checked_abs().unwrap_or(EINVAL)
}

impl Drop for ModuleRuntimeMetadata {
    fn drop(&mut self) {
        let _ = self.release();
    }
}

/// Finish a failed COMING/init transition without ever freeing metadata which
/// a failed notifier teardown still references.  A retained GOING module is
/// intentionally visible to `delete_module()` so userspace can retry the
/// idempotent teardown after the transient failure clears.
fn release_failed_formation(
    module: &Arc<KernelModule>,
    module_name: &str,
    identity: &mut ModuleIdentityReservation,
) {
    let _teardown = module.teardown_lock.lock();
    unexport_module_symbols(module_name);
    match module.runtime_metadata.release() {
        Ok(()) => {
            // As in the normal delete path, architecture registries must be
            // withdrawn explicitly before unlinking. `find_module()` can
            // have handed another caller an Arc while the descriptor was in
            // COMING; relying on the last Arc drop would keep ORC, jump-label,
            // and static-call addresses published for a failed module.
            crate::arch::x86::kernel::module::module_arch_cleanup(&module._arch_metadata);
            MODULES.lock().remove(module_name);
            module.srcu_registration.cleanup();
            module.bug_registration.cleanup();
            identity.release_before_descriptor();
        }
        Err(error) => {
            identity.keep_until_unload();
            crate::log_error!(
                "module",
                "{}: teardown after failed formation returned {}; retaining GOING module for retry",
                module_name,
                error
            );
        }
    }
}

/// Runtime descriptor for a loaded module.
/// Mirrors `struct module` at `include/linux/module.h:397`.
pub struct KernelModule {
    pub name: String,
    /// Address of the relocated C `__this_module` object.
    ///
    /// Linux uses this object as the unique module handle.  Keeping the real
    /// address here is required because vendor drivers pass `THIS_MODULE`
    /// through their public ABI.
    this_module_addr: usize,
    /// x86 finalization state. This precedes `sections` so Linux's
    /// `module_arch_cleanup()` equivalent runs while its section memory is
    /// still retained.
    _arch_metadata: X86ModuleMetadata,
    /// Generic module-notifier and compiler metadata ownership.  This field
    /// precedes `sections` so all global registries are unlinked before their
    /// relocated pointers become invalid.
    runtime_metadata: ModuleRuntimeMetadata,
    /// Serializes cleanup_module(), notifier teardown, retry, and final
    /// descriptor removal.  MODULES protects lookup/publication only; it
    /// cannot be held while callbacks run.
    teardown_lock: SleepingMutex<()>,
    /// Linux module SRCU notifier state. This precedes `sections` so module
    /// static SRCU allocations are released before their owning C objects.
    srcu_registration: ModuleSrcuRegistration,
    /// Linux `module_bug_list` membership. This field precedes `sections` so
    /// drop cleanup unlinks the BUG table before its backing memory is freed.
    bug_registration: ModuleBugRegistration,
    /// Loaded section data.  We keep them alive for the module's lifetime.
    sections: NameMap<LoadedSection>,
    /// Linux `__ksymtab` exports owned by this loaded module.
    pub exported_symbols: Vec<String>,
    /// Configured offsets of `mod->init` and `mod->exit`, recovered from the
    /// modpost relocations rather than assumed from one C layout.
    init_field_offset: Option<usize>,
    exit_field_offset: Option<usize>,
    constructors: Option<ModuleConstructors>,
}

impl KernelModule {
    /// Return the vendor/Linux `struct module *` identity.
    pub fn this_module_addr(&self) -> usize {
        self.this_module_addr
    }

    /// Architecture metadata that was validated and applied while forming
    /// this module.  Exposed for diagnostics and loader acceptance tests.
    pub fn arch_metadata(&self) -> &X86ModuleMetadata {
        &self._arch_metadata
    }

    /// Read `mod->state` from the embedded Linux descriptor.
    pub fn state(&self) -> ModuleState {
        // SAFETY: `this_module_addr` points at the retained, validated
        // `.gnu.linkonce.this_module` allocation owned by `self.sections`.
        unsafe { read_module_state(self.this_module_addr) }
    }

    /// Mirror Linux's stores to `mod->state` during formation and teardown.
    fn set_state(&self, state: ModuleState) {
        // SAFETY: module state transitions are serialized by the module
        // registry and the descriptor remains allocated for this Arc.
        unsafe { write_module_state(self.this_module_addr, state) }
    }

    fn module_function_addr(&self, field_offset: Option<usize>) -> Option<usize> {
        let field_offset = field_offset?;
        let this_module = self.sections.get(".gnu.linkonce.this_module")?;
        let addr = read_u64(this_module.as_slice(), field_offset)?;
        usize::try_from(addr).ok().filter(|addr| *addr != 0)
    }

    /// Read the current `mod->init` value from the embedded C descriptor.
    pub fn init(&self) -> Option<ModuleInitFn> {
        self.module_function_addr(self.init_field_offset)
            .map(|addr| unsafe { core::mem::transmute(addr) })
    }

    /// Read the current `mod->exit` value from the embedded C descriptor.
    pub fn exit(&self) -> Option<ModuleExitFn> {
        self.module_function_addr(self.exit_field_offset)
            .map(|addr| unsafe { core::mem::transmute(addr) })
    }
}

