fn section_data_range(data: &[u8], sh: &Shdr) -> Option<(usize, usize)> {
    let start = usize::try_from(sh.offset).ok()?;
    let size = usize::try_from(sh.size).ok()?;
    let end = start.checked_add(size)?;
    if end > data.len() {
        return None;
    }
    Some((start, end))
}

